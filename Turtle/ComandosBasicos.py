import turtle

screen = turtle.Screen()
tortuga = turtle.Turtle()

tortuga.backward(100) #La tortuga dibuja hacia atrás la distancia específicada
tortuga.right(90) #La tortuga gira a la derecha 90 grados 
tortuga.forward(100) #La tortuga dibuja hacia delante la distancia específicada
tortuga.lt(90) #Left abreviado
tortuga.fd(100) #Forward abreviado
tortuga.left(90)
tortuga.fd(100)

turtle.done()